
mood = input("What mood are you in? ")

if mood == "happy":
    print("It is great to see you happy!")
elif mood == "nervous":
    print("Take a deep breath 3 times.")
elif mood == "sad":
    print("Being sad is okay, too.")
elif mood == "excited":
    print("Chill.")
elif mood == "relaxed":
    print("I'm happy for you!")
else:
    print("I don't recognize this mood.")
